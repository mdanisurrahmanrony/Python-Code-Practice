def sum(num1,num2):
    res = str(num1+num2)
    print("Sum: "+res)

def product(num1,num2):
    res = str(num1*num2)
    print("Product: "+res)
def difference(num1,num2):
    res = str(num1-num2)
    print("Difference: "+res)
num1=int(input("Enter first number: "))
num2=int(input("Enter second number: "))
sum(num1,num2)
product(num1,num2)
difference(num1,num2)